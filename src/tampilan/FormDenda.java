public static void hitungDendaOtomatis() {
    try {
        Connection conn = koneksi.getKoneksi();

        int jumlahBuku = 0;

        String sql = "SELECT COUNT(dp.id_buku) AS jumlah_buku "
           + "FROM pengembalian p "
           + "JOIN isi_peminjaman dp ON p.id_peminjaman = dp.id_peminjaman "
           + "WHERE p.id_pengembalian = ?";

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idpengembalian.getText());

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            jumlahBuku = rs.getInt("jumlah_buku");
        }

        txtjmlhbuku.setText(String.valueOf(jumlahBuku));
        txtdendaperhari.setText(String.valueOf(DENDA_PER_HARI));

        int hariTerlambat = Integer.parseInt(txthariterlambat.getText());
        int totalDenda = jumlahBuku * DENDA_PER_HARI * hariTerlambat;

        txttotaldenda.setText(String.valueOf(totalDenda));

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menghitung denda: " + e.getMessage());
    }
}

public void cetakDenda(String idDenda) {
    try {
        String path = "./src/report/Denda.jasper";

        HashMap parameter = new HashMap();
        parameter.put("id_denda", idDenda);
        parameter.put("denda_per_hari", DENDA_PER_HARI);
        parameter.put("nama_petugas", Session.getNamaPetugas());

        JasperPrint print = JasperFillManager.fillReport(path, parameter, conn);
        JasperViewer.viewReport(print, false);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Gagal mencetak report denda: " + ex);
    }
}

