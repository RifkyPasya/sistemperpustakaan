private void autoID() {
    try {
        Statement st = conn.createStatement();

        ResultSet rs = st.executeQuery(
            "SELECT id_denda FROM denda ORDER BY id_denda DESC LIMIT 1"
        );

        if (rs.next()) {
            String id = rs.getString("id_denda").substring(2);
            int angka = Integer.parseInt(id) + 1;

            String nol = "";

            if (angka < 10) {
                nol = "00";
            } else if (angka < 100) {
                nol = "0";
            }

            txtiddenda.setText("DN" + nol + angka);

        } else {
            txtiddenda.setText("DN001");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}

