    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        if (idpengembalian.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Pilih data pengembalian terlebih dahulu");
        return;
    }

    if (txthariterlambat.getText().equals("") || txttotaldenda.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Data denda belum lengkap");
        return;
    }
    
    if (txtdendaperhari.getText().equals("") || txtjmlhbuku.getText().equals("")) {
    JOptionPane.showMessageDialog(null, "Denda perhari atau jumlah buku belum terisi");
    return;
    }
    
    int hari = Integer.parseInt(txthariterlambat.getText());

    if (hari <= 0) {
        JOptionPane.showMessageDialog(null, "Pengembalian tidak terlambat, tidak ada denda");
        return;
    }

    for (int i = 0; i < model.getRowCount(); i++) {
        if (model.getValueAt(i, 1).toString().equals(idpengembalian.getText())) {
            JOptionPane.showMessageDialog(null, "Denda untuk pengembalian ini sudah ada di tabel");
            return;
        }
    }

    Object[] data = {
        txtiddenda.getText(),
        idpengembalian.getText(),
        txthariterlambat.getText(),
        txttotaldenda.getText()
    };
    
    model.addRow(data);
    idpengembalian.setText("");
    txttglkembali.setText("");
    txttgtkembali.setText("");
    txthariterlambat.setText("");
    txttotaldenda.setText("");
    txtjmlhbuku.setText("");
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        try {
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Klik tombol Tambah terlebih dahulu");
            return;
        }

        String idDenda = model.getValueAt(0, 0).toString();
        String idPengembalian = model.getValueAt(0, 1).toString();
        int hariTerlambat = Integer.parseInt(model.getValueAt(0, 2).toString());
        int totalDenda = Integer.parseInt(model.getValueAt(0, 3).toString());

        String cek = "SELECT * FROM denda WHERE id_pengembalian=?";
        PreparedStatement pstCek = conn.prepareStatement(cek);
        pstCek.setString(1, idPengembalian);
        ResultSet rsCek = pstCek.executeQuery();

        if (rsCek.next()) {
            JOptionPane.showMessageDialog(null, "Denda untuk pengembalian ini sudah pernah disimpan");
            return;
        }

        String sql = "INSERT INTO denda "
                   + "(id_denda, id_pengembalian, hari_terlambat, total_denda) "
                   + "VALUES (?,?,?,?)";

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idDenda);
        pst.setString(2, idPengembalian);
        pst.setInt(3, hariTerlambat);
        pst.setInt(4, totalDenda);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data denda berhasil disimpan");

        cetakDenda(idDenda);

        resetForm();
        tampilData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int baris = tbldenda.getSelectedRow();

    if (baris == -1) {
        JOptionPane.showMessageDialog(null, "Pilih data denda yang ingin dihapus");
        return;
    }

    String idDenda = tbldenda.getValueAt(baris, 0).toString();

    int ok = JOptionPane.showConfirmDialog(
        null,
        "Yakin ingin menghapus data denda ini?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (ok == JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM denda WHERE id_denda=?";

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, idDenda);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data denda berhasil dihapus");

            tampilData();
            resetForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    }//GEN-LAST:event_btnhapusActionPerformed

