    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
    if (txtnama.getText().isEmpty() || txtalamat.getText().isEmpty() || txtnohp.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
        return;
    }

    if (!rblaki.isSelected() && !rbperempuan.isSelected()) {
        JOptionPane.showMessageDialog(null, "Pilih jenis kelamin!");
        return;
    }
    try {
        Connection conn = koneksi.getKoneksi();
        String sql = "INSERT INTO anggota VALUES (?,?,?,?,?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        String jk;
        if (rblaki.isSelected()) {
            jk = "Laki-laki";
        } else {
            jk = "Perempuan";
        }

        pst.setString(1, txtid.getText());
        pst.setString(2, txtnama.getText());
        pst.setString(3, jk);
        pst.setString(4, txtnohp.getText());
        pst.setString(5, txtalamat.getText());
        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

        tampilData();
        resetForm();

    } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void tblanggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblanggotaMouseClicked
        // TODO add your handling code here:
        int baris = tblanggota.getSelectedRow();

        if (baris == -1) {
            return;
        }

        txtid.setText(tblanggota.getValueAt(baris, 0).toString());
        txtnama.setText(tblanggota.getValueAt(baris, 1).toString());
        txtnohp.setText(tblanggota.getValueAt(baris, 2).toString());

        String jk = tblanggota.getValueAt(baris, 3).toString();

        if (jk.equals("Laki-laki")) {
            rblaki.setSelected(true);
        } else {
            rbperempuan.setSelected(true);
        }

        txtalamat.setText(tblanggota.getValueAt(baris, 4).toString());
    }//GEN-LAST:event_tblanggotaMouseClicked

