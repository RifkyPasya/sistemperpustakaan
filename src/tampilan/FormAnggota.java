    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
         int konfirmasi = JOptionPane.showConfirmDialog(null, 
        "Yakin ingin menghapus?", 
        "Konfirmasi", 
        JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = koneksi.getKoneksi();
            String sql = "DELETE FROM anggota WHERE id_anggota=?";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, txtid.getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

            tampilData();
            resetForm();

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
                if (txtnama.getText().isEmpty() || txtalamat.getText().isEmpty() || txtnohp.getText().isEmpty()) {
           JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
           return;
       }

       try {
           Connection conn = koneksi.getKoneksi();

           String jk;
           if (rblaki.isSelected()) {
               jk = "Laki-laki";
           } else if (rbperempuan.isSelected()) {
               jk = "Perempuan";
           } else {
               JOptionPane.showMessageDialog(null, "Pilih jenis kelamin!");
               return;
           }

           String sql = "UPDATE anggota SET nama_anggota=?, no_hp=?, jenis_kelamin=?, alamat=? WHERE id_anggota=?";

           PreparedStatement pst = conn.prepareStatement(sql);

           pst.setString(1, txtnama.getText());
           pst.setString(2, txtnohp.getText());
           pst.setString(3, jk);
           pst.setString(4, txtalamat.getText());
           pst.setString(5, txtid.getText());

           pst.executeUpdate();

           JOptionPane.showMessageDialog(null, "Data berhasil diupdate");

           tampilData();
           resetForm();

       } catch (HeadlessException | SQLException e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
    }//GEN-LAST:event_btneditActionPerformed

