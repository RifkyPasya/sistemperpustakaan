    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("No HP");
    model.addColumn("Jenis Kelamin");
    model.addColumn("Alamat");

    try {
        Connection conn = koneksi.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM anggota");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_anggota"),
                rs.getString("nama_anggota"),
                rs.getString("no_hp"),
                rs.getString("jenis_kelamin"),
                rs.getString("alamat"),
            });
        }

        tblanggota.setModel(model);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    protected void datatable() {
    Object[] Baris = {"Id Anggota", "Nama", "No.Hp", "Jenis Kelamin", "Alamat"};
    DefaultTableModel tabmode = new DefaultTableModel(null, Baris);

    String cari = txtcari.getText();

    try {
        Connection conn = koneksi.getKoneksi();
        Statement stat = conn.createStatement();

        String sql = "SELECT * FROM anggota WHERE " +
                     "id_anggota LIKE '%" + cari + "%' OR " +
                     "nama_anggota LIKE '%" + cari + "%' OR " +
                     "jenis_kelamin LIKE '%" + cari + "%' OR " +
                     "no_hp LIKE '%" + cari + "%'";

        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {
            tabmode.addRow(new Object[]{
                hasil.getString("id_anggota"),
                hasil.getString("nama_anggota"),
                hasil.getString("no_hp"),
                hasil.getString("jenis_kelamin"),
                hasil.getString("alamat"),
            });
        }

        tblanggota.setModel(tabmode);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e.getMessage());
    }
}
     
    private void autoID() {
    try {
        Connection conn = koneksi.getKoneksi();
        Statement st = conn.createStatement();
        String sql = "SELECT id_anggota FROM anggota ORDER BY id_anggota DESC LIMIT 1";
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            String id = rs.getString("id_anggota").substring(2);
            int angka = Integer.parseInt(id) + 1;

            String nol = "";
            if (angka < 10) {
                nol = "00";
            } else if (angka < 100) {
                nol = "0";
            }

            txtid.setText("AG" + nol + angka);
        } else {
            txtid.setText("AG001");
        }

    } catch (NumberFormatException | SQLException e) {
        System.out.println("Auto ID Error: " + e.getMessage());
    }
}
    
