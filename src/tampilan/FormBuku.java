    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
         if (txtjdl.getText().isEmpty() || txtpng.getText().isEmpty() ||
        txtpnb.getText().isEmpty() || txtthntb.getText().isEmpty()) {

        JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
        return;
    }

    try {
        Connection conn = koneksi.getKoneksi();
        String sql = "INSERT INTO buku (id_buku, judul_buku, pengarang, penerbit, tahun_terbit, kategori, status_buku) VALUES (?,?,?,?,?,?,?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, txtid.getText());
        pst.setString(2, txtjdl.getText());
        pst.setString(3, txtpng.getText());
        pst.setString(4, txtpnb.getText());
        pst.setString(5, txtthntb.getText());
        pst.setString(6, cbkat.getSelectedItem().toString());
        pst.setString(7, "Tersedia"); 

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

        tampilData();
        resetForm();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

