    private void tblbukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbukuMouseClicked
        // TODO add your handling code here:
        int baris = tblbuku.getSelectedRow();

        txtid.setText(tblbuku.getValueAt(baris, 0).toString());
        txtjdl.setText(tblbuku.getValueAt(baris, 1).toString());
        txtpng.setText(tblbuku.getValueAt(baris, 2).toString());
        txtpnb.setText(tblbuku.getValueAt(baris, 3).toString());
        String tahun = tblbuku.getValueAt(baris, 4).toString();
        if (tahun.length() >= 4) {
            tahun = tahun.substring(0, 4);
        }
        txtthntb.setText(tahun);
        cbkat.setSelectedItem(tblbuku.getValueAt(baris, 5).toString());
    }//GEN-LAST:event_tblbukuMouseClicked

