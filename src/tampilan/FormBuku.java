    private void tampilData() {

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Judul");
    model.addColumn("Pengarang");
    model.addColumn("Penerbit");
    model.addColumn("Tahun");
    model.addColumn("Kategori");
    model.addColumn("Status");

    String cari = txtcari.getText();

    try {

        Connection conn = koneksi.getKoneksi();
        Statement st = conn.createStatement();

        String sql = "SELECT id_buku, judul_buku, pengarang, penerbit, "
                   + "LEFT(tahun_terbit, 4) AS tahun_terbit, "
                   + "kategori, status_buku "
                   + "FROM buku WHERE "
                   + "id_buku LIKE '%" + cari + "%' OR "
                   + "judul_buku LIKE '%" + cari + "%' OR "
                   + "pengarang LIKE '%" + cari + "%' OR "
                   + "penerbit LIKE '%" + cari + "%' OR "
                   + "LEFT(tahun_terbit, 4) LIKE '%" + cari + "%' OR "
                   + "kategori LIKE '%" + cari + "%' OR "
                   + "status_buku LIKE '%" + cari + "%'";

        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getString("id_buku"),
                rs.getString("judul_buku"),
                rs.getString("pengarang"),
                rs.getString("penerbit"),
                rs.getString("tahun_terbit"),
                rs.getString("kategori"),
                rs.getString("status_buku")
            });
        }

        tblbuku.setModel(model);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}
    
    private void autoID() {
    try {
        Connection conn = koneksi.getKoneksi();
        Statement st = conn.createStatement();
        String sql = "SELECT id_buku FROM buku ORDER BY id_buku DESC LIMIT 1";
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            String id = rs.getString("id_buku").substring(2);
            int angka = Integer.parseInt(id) + 1;

            String nol = "";
            if (angka < 10) {
                nol = "00";
            } else if (angka < 100) {
                nol = "0";
            }

            txtid.setText("BK" + nol + angka);
        } else {
            txtid.setText("BK001");
        }

    } catch (Exception e) {
        System.out.println("Auto ID Error: " + e.getMessage());
    }
}
    
