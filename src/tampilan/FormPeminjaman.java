    private void autoID() {
    try {
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT id_peminjaman FROM peminjaman ORDER BY id_peminjaman DESC LIMIT 1"
        );

        if(rs.next()){

            String id = rs.getString("id_peminjaman").substring(2);

            int angka = Integer.parseInt(id) + 1;

            String nol = "";

            if(angka < 10){
                nol = "00";
            }else if(angka < 100){
                nol = "0";
            }

            txtidpinjam.setText("PJ"+nol+angka);

        }else{

            txtidpinjam.setText("PJ001");
        }

    } catch(Exception e){
        JOptionPane.showMessageDialog(null,e.getMessage());
    }
}
    
