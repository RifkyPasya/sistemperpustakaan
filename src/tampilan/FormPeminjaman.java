public void cetakpeminjaman(String idPeminjaman) {
    try {
        String path = "./src/report/Peminjaman.jasper";

        HashMap parameter = new HashMap();
        parameter.put("id_peminjaman", idPeminjaman);
        parameter.put("nama_petugas", Session.getNamaPetugas());

        JasperPrint print = JasperFillManager.fillReport(path, parameter, conn);
        JasperViewer.viewReport(print, false);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Gagal mencetak report: " + ex);
    }
}

