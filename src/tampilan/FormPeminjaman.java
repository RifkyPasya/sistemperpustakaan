    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String idPeminjaman = txtidpinjam.getText();

    try {
        if (txtidanggota.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih anggota terlebih dahulu");
            return;
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Tambahkan buku terlebih dahulu");
            return;
        }

        SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");

        String tanggal = fm.format(sptanggal.getValue());
        String tenggat = fm.format(sptenggat.getValue());

        conn.setAutoCommit(false);

        String sqlPeminjaman =
            "INSERT INTO peminjaman " +
            "(id_peminjaman, tanggal_pinjam, id_anggota, id_petugas, tenggat_kembali) " +
            "VALUES (?,?,?,?,?)";

        PreparedStatement pst = conn.prepareStatement(sqlPeminjaman);
        pst.setString(1, idPeminjaman);
        pst.setString(2, tanggal);
        pst.setString(3, txtidanggota.getText());
        pst.setString(4, Session.getIdPetugas());
        pst.setString(5, tenggat);
        pst.executeUpdate();

        String sqlIsi =
            "INSERT INTO isi_peminjaman (id_peminjaman, id_buku) VALUES (?,?)";

        String sqlUpdateBuku =
            "UPDATE buku SET status_buku='Dipinjam' WHERE id_buku=?";

        for (int i = 0; i < model.getRowCount(); i++) {
            String idBuku = model.getValueAt(i, 1).toString();

            PreparedStatement pstIsi = conn.prepareStatement(sqlIsi);
            pstIsi.setString(1, idPeminjaman);
            pstIsi.setString(2, idBuku);
            pstIsi.executeUpdate();

            PreparedStatement pstUpdate = conn.prepareStatement(sqlUpdateBuku);
            pstUpdate.setString(1, idBuku);
            pstUpdate.executeUpdate();
        }

        conn.commit();

        JOptionPane.showMessageDialog(null, "Data peminjaman berhasil disimpan");

        cetakpeminjaman(idPeminjaman);

        resetForm();
        tampilData();

    } catch (Exception e) {
        try {
            conn.rollback();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        JOptionPane.showMessageDialog(null, e.getMessage());

    } finally {
        try {
            conn.setAutoCommit(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

